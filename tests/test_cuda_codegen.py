def test_cuda_barrier_uses_syncthreads():
    def k(X: locomp.Tensor, Y: locomp.Tensor, N: locomp.constexpr):
        smem = locomp.shared_memory(64)
        tid = locomp.local_id(0)
        v = locomp.load(X + tid)
        locomp.shared_store(smem, tid, v)
        locomp.barrier()
        out = locomp.shared_load(smem, tid)
        locomp.store(Y + tid, out)

    src, _ = _codegen(k, {"N": 64})
    assert "__syncthreads()" in src


def test_cuda_tiled_load_uses_loop():
    def k(A: locomp.Tensor, Out: locomp.Tensor, N: locomp.constexpr):
        i = locomp.program_id(0)
        offs = locomp.arange(0, 64)
        a = locomp.load(A + i * 64 + offs)
        locomp.store(Out + i * 64 + offs, a)

    src, _ = _codegen(k, {"N": 64})
    # CUDA tiled load emits a loop (not RVV intrinsics)
    assert "for" in src
    assert "[_i]" in src


