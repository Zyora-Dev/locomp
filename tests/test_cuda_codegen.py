def test_cuda_has_headers():
    def vec_add(A: locomp.Tensor, B: locomp.Tensor, Out: locomp.Tensor, N: locomp.constexpr):
        i = locomp.program_id(0)
        a = locomp.load(A + i)
        b = locomp.load(B + i)
        locomp.store(Out + i, a + b)

    src, _ = _codegen(vec_add, {"N": 1024})
    assert "#include <stdint.h>" in src
    assert "#include <cuda_fp16.h>" in src
    assert "Generated by locomp" in src


