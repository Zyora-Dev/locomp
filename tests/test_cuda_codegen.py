def test_cuda_constexpr_inlined():
    def k(X: locomp.Tensor, Out: locomp.Tensor, N: locomp.constexpr):
        i = locomp.program_id(0)
        acc = 0.0
        for j in range(N):
            v = locomp.load(X + i * N + j)
            acc = acc + v
        locomp.store(Out + i, acc)

    src, param_map = _codegen(k, {"N": 512})
    # N should not appear as a kernel parameter
    assert "N" not in param_map
    # N inlined as 512 in the loop bound
    assert "512" in src


