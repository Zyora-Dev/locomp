def test_cuda_if_else_codegen():
    def k(X: locomp.Tensor, Out: locomp.Tensor, N: locomp.constexpr):
        i = locomp.program_id(0)
        v = locomp.load(X + i)
        if v > 0.0:
            locomp.store(Out + i, v)
        else:
            locomp.store(Out + i, 0.0)

    src, _ = _codegen(k, {"N": 64})
    assert "if (" in src
    assert "} else {" in src


def test_cuda_param_map_excludes_constexpr():
    def k(A: locomp.Tensor, B: locomp.Tensor, Out: locomp.Tensor,
          N: locomp.constexpr, D: locomp.constexpr):
        i = locomp.program_id(0)
        a = locomp.load(A + i)
        locomp.store(Out + i, a)

    _, param_map = _codegen(k, {"N": 32, "D": 16})
    assert "N" not in param_map
    assert "D" not in param_map
    assert "A" in param_map
    assert "B" in param_map
    assert "Out" in param_map
